class Course {
  final String images;
  final String title;
  final String duration;
  final String rating;
  final String price;
  final bool isBestseller;
  final int index;
  final String category;

  Course({
    required this.images,
    required this.title,
    required this.duration,
    required this.rating,
    required this.price,
    required this.isBestseller,
    required this.index,
    required this.category,
  });
}
